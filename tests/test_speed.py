    @staticmethod
    def execute_and_get_stats(backend, qc, shots):
        job = execute(
            qc,
            optimization_level=0,
            backend=backend,
            shots=shots,
            seed_simulator=1,
        )
        job_result = job.result()
        counts = job_result.get_counts(qc)
        total_counts = 0
        for c in counts:
            total_counts += counts[c]
        ret = dict()
        ret["counts"] = counts
        ret["totalcounts"] = total_counts
        return ret

